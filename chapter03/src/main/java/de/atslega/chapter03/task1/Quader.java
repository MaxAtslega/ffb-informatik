package de.atslega.chapter03.task1;

public class Quader {
    double seiteA;
    double seiteB;
    double seiteC;

    public Quader(double seiteA, double seiteB, double seiteC) {
        this.seiteA = seiteA;
        this.seiteB = seiteB;
        this.seiteC = seiteC;
    }

    public void setSeiteA(double seiteA) {
        this.seiteA = seiteA;
    }

    public void setSeiteB(double seiteB) {
        this.seiteB = seiteB;
    }

    public void setSeiteC(double seiteC) {
        this.seiteC = seiteC;
    }
}
